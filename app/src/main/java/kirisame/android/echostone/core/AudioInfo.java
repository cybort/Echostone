public class AudioInfo {

    /**
     * 歌曲标题
     */
    public String title;

    /**
     * 歌曲所属专辑
     */
    public String album;

    /**
     * 歌曲艺术家
     */
    public String artist;

    /**
     * 歌曲位置
     */
    public String url;

    /**
     * 歌曲长度
     */
    public int duration;

    /**
     * 歌曲大小
     */
    public long size;

    /**
     * 歌曲ID
     */
    public int id;
}
